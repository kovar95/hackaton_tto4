export const findPossibleCombinations = (selectedFields, winC) => {
  let myPossWinComb;
  let movesToWin = 4;
  
  if (winC.length === 0) {
    myPossWinComb = {
      id: 0,
      com: [],
      movesToWin: 4,
    };
  } else {
    winC.forEach((wc) => {
      let combin = [];
      wc.combination.forEach((c) => {
        selectedFields.forEach((mc) => {
          if (mc.x === c.x && mc.y === c.y) {
            combin.push(mc);
          }
        });
      });
      if (4 - combin.length < movesToWin) {
        movesToWin = 4 - combin.length;
        myPossWinComb = {
          id: wc.id,
          com: combin,
          movesToWin: 4 - combin.length,
        };
      }
    });
  }
  
  return myPossWinComb;
};

export const updateTempTable = (table, selectedField, owner) => {
  let tempBoard = table.filter((f) => {
    if (f.filed.x === selectedField.x && f.filed.y === selectedField.y) {
      f.owner = owner;
    }
    return f;
  });
  return tempBoard;
}